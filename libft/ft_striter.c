#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	int		a;
	char	*d;

	if (s && f)
	{
		a = 0;
		while (s[a] != '\0')
		{
			f(&s[a]);
			a++;
		}
	}
}
